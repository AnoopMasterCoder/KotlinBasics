package com.anoop.kotlinbasics

fun main() {


//  General Structure of if-else statement
//    if(true){
//        // Then execute what's in here
//    }else{
//        // If not true, then  execute this in here
//    }

    var myAge = 15

    /*
    greater than (>)
    lower than (<)
    greater than or equal to (>=)
    lower than or equal to (<=)
    equal to (==)
    */

    if (myAge >= 18) {
        println("You can go into the club")
    } else {
        println("You are too young to go into the club!")
    }
}