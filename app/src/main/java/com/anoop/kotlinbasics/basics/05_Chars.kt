package com.anoop.kotlinbasics.basics

fun main() {

    val myChar: Char = 'a' // `Char` can store only ONE character
//  val myChar = 'fd'      // Throws an error

    /*
    * Special characters start from an escaping backslash \. The following escape sequences are supported:

        \t – tab

        \b – backspace

        \n – new line (LF)

        \r – carriage return (CR)

        \' – single quotation mark

        \" – double quotation mark

        \\ – backslash

        \$ – dollar sign
     */
    println(myChar)

}